    private void BtnTebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTebakActionPerformed
        // TODO add your handling code here:
        System.out.println(""+AngkaTargetan);
        try {
            int tebakan = Integer.parseInt(CobaTebak.getText());
        if (tebakan < 1 || tebakan > 100) {
            JOptionPane.showMessageDialog(this, "Masukkan Angka Antara 1 - 100!", "Peringatan", 
                    JOptionPane.WARNING_MESSAGE);
            CobaTebak.setText(""); 
            return;
        }            
            Peluang--;
            if (tebakan < AngkaTargetan) {
                Cek.setText("Angka Tebakan Terlalu Kecil !!");
            } else if (tebakan > AngkaTargetan) {
                Cek.setText("Angka Tebakan Terlalu Besar !!");
            } else {
                JOptionPane.showMessageDialog(this, "Selamat, Anda Telah Berhasil");
                String nama = JOptionPane.showInputDialog(this, "Silahkan Masukkan Nama Anda:");
                int score = Peluang * 10;
                String skornya = nama + " - Score : " + score;
                ScoreModel.addElement(skornya);
                Score.setText("" + score);
                Cek.setText("Saya Adalah Angka : " + AngkaTargetan);
                ResetGames();
            }

            BtnTebak.setText("Tebak(" + Peluang + ")");
            
            if (Peluang <= 0) {
                JOptionPane.showMessageDialog(this, "Anda Telah Gagal, Dan Kesempatan Anda Telah Habis.");
                Score.setText("0");
                ResetGames();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan Angka Yang Valid!", "Error", 
                    JOptionPane.ERROR_MESSAGE);
            CobaTebak.setText("");
        }
    }//GEN-LAST:event_BtnTebakActionPerformed

